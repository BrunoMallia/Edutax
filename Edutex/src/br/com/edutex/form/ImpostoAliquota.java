package br.com.edutex.form;

public class ImpostoAliquota {
	
	private String ncm;
	
	private double aliquotaICMS;
	
	private double aliquotaPIS;
	
	private double aliquotaCOFINS;
	
	private double aliquotaIPI;
	
	private double aliquotaICMSST;
	
	private double percentualReducao;

	public String getNcm() {
		return ncm;
	}

	public double getAliquotaICMS() {
		return aliquotaICMS;
	}

	public double getAliquotaPIS() {
		return aliquotaPIS;
	}

	public double getAliquotaCOFINS() {
		return aliquotaCOFINS;
	}

	public double getAliquotaIPI() {
		return aliquotaIPI;
	}

	public double getAliquotaICMSST() {
		return aliquotaICMSST;
	}

	public double getPercentualReducao() {
		return percentualReducao;
	}

	public void setNcm(String ncm) {
		this.ncm = ncm;
	}

	public void setAliquotaICMS(double aliquotaICMS) {
		this.aliquotaICMS = aliquotaICMS;
	}

	public void setAliquotaPIS(double aliquotaPIS) {
		this.aliquotaPIS = aliquotaPIS;
	}

	public void setAliquotaCOFINS(double aliquotaCOFINS) {
		this.aliquotaCOFINS = aliquotaCOFINS;
	}

	public void setAliquotaIPI(double aliquotaIPI) {
		this.aliquotaIPI = aliquotaIPI;
	}

	public void setAliquotaICMSST(double aliquotaICMSST) {
		this.aliquotaICMSST = aliquotaICMSST;
	}

	public void setPercentualReducao(double percentualReducao) {
		this.percentualReducao = percentualReducao;
	}

	

	
	
}
